	// PART 1
	public static int[][] createGrid(int[][]rect, int[][] cloth) {
		// iterate over each of the claims
		for (int i = 0; i < rect.length; i++) {
			
			// iterate over the height of the rectangle
			for (int j = 0; j < rect[i][4]; j++) {
				
				// iterate over the length of the rectangle
				for (int k = 0; k < rect[i][3]; k++) {
					
					cloth[rect[i][2] + j][rect[i][1] + k]++;
				
				}
			}
			
		}
		return cloth;
	}
	
	public static int getOverlap(int[][] rect, int[][] cloth) {
		
		// return number of overlapping square inches
		int overlap = 0;
		for (int i = 0; i < cloth.length; i++) {
			for (int j = 0; j < cloth[i].length; j++) {
				if (cloth[i][j] > 1) {
					overlap++;
				}
				// System.out.print(cloth[i][j] + " ");
			}
			// System.out.println();
		}
		
		return overlap;
		
	}
	
	// PART 2
	public static int getNoOverlap(int[][] rect, int[][] cloth) {
		
		// check each claim
		for (int claimCheck = 0; claimCheck < rect.length; claimCheck++) {
			
			// check claim vs. the grid of overlapping cloth
			boolean noOverlap = true;
			
			// iterate over the height of the rectangle
			for (int j = 0; j < rect[claimCheck][4]; j++) {
				
				// iterate over the length of the rectangle
				for (int k = 0; k < rect[claimCheck][3]; k++) {
					
					if (cloth[rect[claimCheck][2] + j][rect[claimCheck][1] + k] > 1) {
						noOverlap = false;
					}
				
				}
			}
			
			if (noOverlap) {
				return rect[claimCheck][0];
			}
			
		}
		
		return 0;
		
	}

